import UIKit

extension UIFont {
    
    /// useful macros
    
    class func sfProTextdLight(size s: CGFloat) -> UIFont {
        return UIFont(name: "SFProText-Light", size: s)!
    }
    
    class func sfProTextBold(size s: CGFloat) -> UIFont {
        return UIFont(name: "SFProText-Bold", size: s)!
    }
    
    class func sfProTextMedium(size s: CGFloat) -> UIFont {
        return UIFont(name: "SFProText-Medium", size: s)!
    }
    
    class func sfProTextRegular(size s: CGFloat) -> UIFont {
        return UIFont(name: "SFProText-Regular", size: s)!
    }
    
    class func sfProTextSemibold(size s: CGFloat) -> UIFont {
        return UIFont(name: "SFProText-Semibold", size: s)!
    }
    
    class func syababFREETESTER(size s: CGFloat) -> UIFont {
        return UIFont(name: "SyababFREETESTER", size: s)!
    }
    
    class func uniformBold(size s: CGFloat) -> UIFont {
        return UIFont(name: "Uniform-Bold", size: s)!
    }
    
}
